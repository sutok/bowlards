          <DialogTitle>„Ç≤„Éº„É†„ÇíÂâäÈô§</DialogTitle>
          <DialogContent>
            <Typography>
              „Åì„ÅÆ„Ç≤„Éº„É†„ÇíÂâäÈô§„Åó„Å¶„ÇÇ„Çà„Çç„Åó„ÅÑ„Åß„Åô„ÅãÔºü„Åì„ÅÆÊìç‰Ωú„ÅØÂèñ„ÇäÊ∂à„Åõ„Åæ„Åõ„Çì„ÄÇ
